function repairStructure(html) {
  let out = html;

  out = out.replace(
    /E\/(option|label|p|span|h1|h2|h3|h4|strong|a|div|li|ul|section|header|footer|button|textarea|select|table|tr|td|th|em|small|time|code)>/gi,
    "</$1>"
  );

  out = out.replace(/忁E��E\/span>/g, "必須</span>");
  out = out.replace(/◁E\/span>/g, "◆</span>");

  out = out.replace(
    /<option value="([^"]*?)E>([^<]*)<\/option>/gi,
    (_m, _v, text) => {
      const clean = String(text || "")
        .replace(/E\/option$/i, "")
        .replace(/E$/i, "")
        .trim();
      const esc = clean.replace(/"/g, "&quot;");
      return `<option value="${esc}">${clean}</option>`;
    }
  );

  out = out.replace(/value="([^"]*?)E>/g, (_m, val) => {
    const clean = String(val).replace(/E$/i, "").trim();
    return `value="${clean}">`;
  });

  out = out.replace(/>([^<]*?)E\/option>/gi, ">$1</option>");

  return out;
}

function repairText(html) {
  let out = html;
  for (const [from, to] of REPLACEMENTS) {
    if (from) out = out.split(from).join(to);
  }
  return out;
}

function validateHtml(html) {
  const errors = [];
  try {
    parse5.parse(html);
  } catch (err) {
    errors.push(String(err.message || err));
  }

  const structural = [
    [/E\/option>/gi, "E/option>"],
    [/E\/label>/gi, "E/label>"],
    [/E\/p>/gi, "E/p>"],
    [/E\/span>/gi, "E/span>"],
    [/<select\b/gi, "<select"],
    [/<\/select>/gi, "</select>"],
  ];

  for (const [re] of structural.slice(0, 4)) {
    const n = (html.match(re) || []).length;
    if (n > 0) errors.push(`${re}: ${n} remaining`);
  }

  const opens = (html.match(/<select\b/gi) || []).length;
  const closes = (html.match(/<\/select>/gi) || []).length;
  if (opens !== closes) errors.push(`select mismatch open=${opens} close=${closes}`);

  const mojibake = (html.match(/[\u0080-\u009f]|�|E��|ã��/g) || []).length;

  return { errors, mojibake };
}

